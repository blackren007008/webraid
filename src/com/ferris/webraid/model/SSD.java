package com.ferris.webraid.model;

public class SSD {
	private String id;
	private String seqNo;
	private String capcity;
	private String ssdName;
	private String mediaType;
	private String firmwareVer;
	private String model;
	private String status;
	private String manufactCompany;
	private String busProtocol;
	private String storagePool;
	private String deviceNo;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSeqNo() {
		return seqNo;
	}
	public void setSeqNo(String seqNo) {
		this.seqNo = seqNo;
	}
	public String getCapcity() {
		return capcity;
	}
	public void setCapcity(String capcity) {
		this.capcity = capcity;
	}
	public String getSsdName() {
		return ssdName;
	}
	public void setSsdName(String ssdName) {
		this.ssdName = ssdName;
	}
	public String getMediaType() {
		return mediaType;
	}
	public void setMediaType(String mediaType) {
		this.mediaType = mediaType;
	}
	public String getFirmwareVer() {
		return firmwareVer;
	}
	public void setFirmwareVer(String firmwareVer) {
		this.firmwareVer = firmwareVer;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getManufactCompany() {
		return manufactCompany;
	}
	public void setManufactCompany(String manufactCompany) {
		this.manufactCompany = manufactCompany;
	}
	public String getBusProtocol() {
		return busProtocol;
	}
	public void setBusProtocol(String busProtocol) {
		this.busProtocol = busProtocol;
	}
	public String getStoragePool() {
		return storagePool;
	}
	public void setStoragePool(String storagePool) {
		this.storagePool = storagePool;
	}
	public String getDeviceNo() {
		return deviceNo;
	}
	public void setDeviceNo(String deviceNo) {
		this.deviceNo = deviceNo;
	}
	
}
